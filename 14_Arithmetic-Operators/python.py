# --------------------------
# -- Arithmetic Operators --
#       
#       [+] Addition
#       [-] Subtraction
#       [*] Multiplication
#       [/] Division
#       [%] Modules         (The Rest of Division)
#       [**] Exponent
#       [//] Floor Division     Floor the Float Number
# --------------------------

# Addition

print(10+20)

# Subtraction

print(20-15)

# Multiplication

print(2*5)
print(5 + 10 * 100)         # 1005
print((5 + 10) * 100)       # 1500

# Division

print(100 / 20)
print(int(100/20))

# Modules

print(8 % 2)    # 0
print(9 % 2)    # 1
print(22 % 5)   # 2

# Exponent

print(2 ** 5)               # 32
print(2 * 2 * 2 * 2 * 2)    # 32
print(5 ** 4)           #625
print(5 * 5 * 5 * 5)    # 625

print("#-------------------------------#")
# Floor Division

print(100 // 20)       # 5
print(110 // 20)       # 5
print(99 // 20)        # 4

print(119 / 20)       # 5
print(120 // 20)       # 6
print(140 // 20)       # 7