# ------------------------------
#   -- Comparison Operators --

#       1- [ == ]    =>  Equal
#       2- [ != ]    =>  Not Equal
#       3- [ > ]     =>  Greater Than
#       4- [ < ]     =>  Less Than
#       5- [ >= ]    =>  Greater Than Or Equal
#       6- [ < ]     =>  Less Than Or Equal
# ------------------------------

print(10 == 10)
print(10 != 100)
print(10 > 100)
print(10 < 20)
print(10 >= 100)
print(10 <= 100)