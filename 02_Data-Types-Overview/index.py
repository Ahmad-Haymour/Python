# ----------------
# type()
# All Data in Python is Object
# ----------------

## int
print(type(100))  # int  => Integer
print(50)    # int  => Integer
print(-50)   # int  => Integer

## float
print(type(100.9))  # float  => Floating Point Number
print(type(1.213456729))    # float  => Floating Point Number
print(type(-500.589))   # float  => Floating Point Number

## str
print(type("Hello Python"))   # str  => String

## list
print(type( [ 1, 2, 3, 4] ))    # list  =>  List  (Array)

## tuple
print(type( ( 1, 2, 3, 4, 5) )) # tuple  => Tuple   (Group)

## dict
print( type( { "One": 1, "Two": 2, "Three": 3 } ))  # dict  => Dictionary   (Object)